void insertAthlete(int athletesFd, int athleteId, char *athleteSurname, char *athleteName) {

	int recId;
	char *athleteRec;

	athleteRec = (char*) malloc(HF_MAIN_ATHLETE_REC_SIZE);
	if (athleteRec == NULL)
	{
		printf("Athlete Record malloc failed\n");
		return;
	}

	printf("Inserting Athlete record (%d, \"%s\", \"%s\")\n", athleteId, athleteName, athleteSurname);

	memcpy((void *) athleteRec, (void *) &athleteId, sizeof(int));
	athleteRec += sizeof(int);
	memcpy((void *) athleteRec, (void *) athleteSurname, HF_MAIN_NAME_SIZE);
	athleteRec += HF_MAIN_NAME_SIZE;
	memcpy((void *) athleteRec, (void *) athleteName, HF_MAIN_NAME_SIZE);
	athleteRec += HF_MAIN_NAME_SIZE;

	athleteRec -= HF_MAIN_ATHLETE_REC_SIZE;

	recId = HF_InsertRec(athletesFd, athleteRec, HF_MAIN_ATHLETE_REC_SIZE);

	if (recId < 0)
	{
		HF_PrintError("Athlete not inserted.\n");
	}

	free(athleteRec);
}



